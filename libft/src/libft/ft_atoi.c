#include "libft.h"

int	ft_atoi(const char *s)
{
	int	res;
	int	sign;

	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	sign = 1;
	if (*s == '-')
		sign = -1;
	if (*s == '+' || *s == '-')
		s++;
	res = 0;
	while (*s >= '0' && *s <= '9')
		res = (res * 10) + (*s++ - '0');
	return (res * sign);
}
