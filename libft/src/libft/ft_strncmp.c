#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*us1;
	unsigned char	*us2;
	size_t			i;

	us1 = (unsigned char *) s1;
	us2 = (unsigned char *) s2;
	i = 0;
	while (us1[i] == us2[i] && i < n && (us1[i] && us2[i]))
		i++;
	if (i == n)
		return (0);
	return (us1[i] - us2[i]);
}
